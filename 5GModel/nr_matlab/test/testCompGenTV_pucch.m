                case {6606, 6607, 6608, 6609, 6610, 6611, 6612, 6613, 6614, 6615} % -10dB Rx power
                    SysPar.Chan{1}.gain = sqrt(0.1);
                case {6616, 6617, 6618, 6619, 6620, 6621, 6622, 6623, 6624, 6625} % 0dB Rx power
                    SysPar.Chan{1}.gain = 1;
                case {6626, 6627, 6628, 6629, 6630, 6631, 6632, 6633, 6634, 6635} % 10dB Rx power
                    SysPar.Chan{1}.gain = sqrt(10);
                otherwise
            end
            
            switch caseNum
                case {6606, 6616, 6626}
                    SysPar.Chan{1}.SNR = -10 - 20*log10(SysPar.Chan{1}.gain); % -10 dB SNR
                case {6607, 6617, 6627}
                    SysPar.Chan{1}.SNR = -5  - 20*log10(SysPar.Chan{1}.gain); % -5 dB SNR
                case {6608, 6618, 6628}
                    SysPar.Chan{1}.SNR =     - 20*log10(SysPar.Chan{1}.gain); % 0 dB SNR
                case {6609, 6619, 6629}
                    SysPar.Chan{1}.SNR = 5   - 20*log10(SysPar.Chan{1}.gain); % 5 dB SNR
                case {6610, 6620, 6630}
                    SysPar.Chan{1}.SNR = 10  - 20*log10(SysPar.Chan{1}.gain); % 10 dB SNR    
                case {6611, 6621, 6631}
                    SysPar.Chan{1}.SNR = 15  - 20*log10(SysPar.Chan{1}.gain); % 15 dB SNR
                case {6612, 6622, 6632}
                    SysPar.Chan{1}.SNR = 20  - 20*log10(SysPar.Chan{1}.gain); % 20 dB SNR
                case {6613, 6623, 6633}    
                    SysPar.Chan{1}.SNR = 25  - 20*log10(SysPar.Chan{1}.gain); % 25 dB SNR
                case {6614, 6624, 6634}    
                    SysPar.Chan{1}.SNR = 30  - 20*log10(SysPar.Chan{1}.gain); % 30 dB SNR
                case {6615, 6625, 6635}    
                    SysPar.Chan{1}.SNR = 35  - 20*log10(SysPar.Chan{1}.gain); % 35 dB SNR
                otherwise   
            end
            
            %% F1 TA estimation tests 
            if ismember(caseNum, [6175:6192])
                SysPar.SimCtrl.timeDomainSim = 1;
                SysPar.Chan{1}.SNR = 3*(mod(caseNum - 6175, 10) + 1); 
                SysPar.Chan{1}.CFO = 300;
                SysPar.Chan{1}.delay = mod(caseNum - 6175, 10)*2e-7;
            end
            
            %% F2 TA estimation tests 
            if ismember(caseNum, [6275:6298])
                SysPar.SimCtrl.timeDomainSim = 1;
                SysPar.Chan{1}.SNR = 3*(mod(caseNum - 6275, 10) + 1); 
                SysPar.Chan{1}.CFO = 300;
                SysPar.Chan{1}.delay = mod(caseNum - 6275, 10)*2e-7;
            end
            
            %% F3 TA estimation tests
            if ismember(caseNum, [6375:6399])
                SysPar.SimCtrl.timeDomainSim = 1;
                SysPar.Chan{1}.SNR = 3*(mod(caseNum - 6375, 10) + 1); 
                SysPar.Chan{1}.CFO = 300;
                SysPar.Chan{1}.delay = mod(caseNum - 6375, 10)*2e-7;
            end

            %% PF1 and PF3 TCs per request
            if caseNum == 6724
                SysPar.pucch{1}.RNTI = 46;
            elseif caseNum == 6725
                SysPar.pucch{1}.RNTI = 1;
            elseif caseNum == 6726
                SysPar.pucch{1}.RNTI = 32767;
            elseif caseNum == 6727
                SysPar.pucch{1}.RNTI = 65535;
            elseif caseNum == 6728
                SysPar.pucch{1}.RNTI = 46;
                SysPar.pucch{1}.hoppingId = 274;
            elseif caseNum == 6729
                SysPar.pucch{1}.RNTI = 65535;
                SysPar.pucch{1}.hoppingId = 273;
            elseif caseNum == 6730
                SysPar.pucch{1}.RNTI = 1;
                SysPar.pucch{1}.hoppingId = 0;
            elseif caseNum == 6731
                SysPar.pucch{1}.RNTI = 32767;
                SysPar.pucch{1}.hoppingId = 1;
            elseif caseNum == 6732
                SysPar.pucch{1}.RNTI = 46;
            elseif caseNum == 6733
                SysPar.pucch{1}.RNTI = 1;
            elseif caseNum == 6734
                SysPar.pucch{1}.RNTI = 32767;
            elseif caseNum == 6735
                SysPar.pucch{1}.RNTI = 65535; 
            end
        end

        if caseNum == 6806
            SysPar.carrier.duplex = 0;
            SysPar.carrier.N_grid_size_mu = 106;
            SysPar.prach{1}.configurationIndex = 198;
            SysPar.testAlloc.prach = 0;
        end
        
        if ismember(caseNum, [6636:6639])
            SysPar.SimCtrl.BFPforCuphy = 16;
        elseif ismember(caseNum, [6640:6643])
            SysPar.SimCtrl.BFPforCuphy = 14;
        elseif ismember(caseNum, [6644:6647])
            SysPar.SimCtrl.BFPforCuphy = 9; 
        elseif ismember(caseNum, [6652:6655])
            SysPar.SimCtrl.FixedPointforCuphy = 16;
        end

        if caseNum == 6736 || caseNum == 6737
            for idxPucchPdu = 1:10
                SysPar.pucch{idxPucchPdu}.RNTI = idxPucchPdu;
            end
        end

        if caseNum == 6741
            SysPar.SimCtrl.force_pucch_payload_as = 'zeros';
        end
        if caseNum == 6743 % HARQ ACK/NACK + SR
            SysPar.pucch{2}.BitLenSr = 1;
            SysPar.pucch{2}.BitLenCsiPart1 = 0;
            SysPar.pucch{2}.maxCodeRate = 4;
        end
        
        for idxUe = 1:length(SysPar.pucch)
            digBFInterfaces = SysPar.carrier.Nant_gNB;
            SysPar.pucch{idxUe}.digBFInterfaces = digBFInterfaces;
            SysPar.pucch{idxUe}.beamIdx = [1:digBFInterfaces];
        end
        
        % update Nre_max which is used to calculate beta value in BFP
        SysPar.SimCtrl.oranComp.Nre_max = SysPar.carrier.N_grid_size_mu*12;
        
        % save SysPar into Cfg_<TC#>.yaml config file
        if SysPar.SimCtrl.genTV.genYamlCfg
            fileName = sprintf('Cfg_%04d.yaml', caseNum);
            WriteYaml(fileName, SysPar);
        end
        
        if SysPar.SimCtrl.genTV.enable && SysPar.SimCtrl.genTV.launchPattern
            if ~ismember(caseNum, disabled_TC)
                LPFileName = 'launch_pattern_nrSim';
            else
                LPFileName = 'disabled_launch_pattern_nrSim';
            end
            slotIdx =  SysPar.SimCtrl.genTV.slotIdx;
            genSingleSlotLPFile(LPFileName, caseNum, slotIdx);
        end
        
        [SysPar, UE, gNB] = nrSimulator(SysPar);        
        SimCtrl = SysPar.SimCtrl; 
        
        results = SimCtrl.results.pucch;
        Detected = 1;
        nPucch = length(results);
        for idxPucch = 1:nPucch
            if results{idxPucch}.errorCnt > 0 || results{idxPucch}.detStatError > 0
                Detected = 0;
                break;
            end
        end 
        
        % bypass detection check for forceRxZero cases
        if ismember(caseNum, [6595, 6598, 6601, 6604])
            Detected = 1;
        end

        if ~Detected
            detErr = detErr + 1;
        end
        testPass = 1;
        if testCompliance            
            nComp = nComp + 1;            
            for idxUe = 1:nUe                
                Xtf_nr = UE{idxUe}.Phy.tx.Xtf;
                sym_nr = [];  
                sym_dmrs_nr = [];
                DTX = SysPar.pucch{idxUe}.DTX;
                
                if (SysPar.pucch{idxUe}.FormatType == 2)
                    if ~DTX
                        startSym = SysPar.pucch{idxUe}.startSym + 1;
                        startPRB = SysPar.pucch{idxUe}.prbStart+1;
                        startCRB = startPRB+SysPar.pucch{idxUe}.BWPStart;
                        nPRBs = SysPar.pucch{idxUe}.prbSize;
                        
                        data_fIdx_base = transpose([0 2 3 5 6 8 9 11]);
                        data_fIdx_offset = kron(transpose(startCRB:startCRB+nPRBs-1),ones(8,1))*12 - 11; % 1-based indexing
                        data_fIdx = repmat(data_fIdx_base,nPRBs,1) + data_fIdx_offset;
                        sym_nr = Xtf_nr(data_fIdx, startSym);

                        dmrs_fIdx_base = transpose([1 4 7 10]);
                        dmrs_fIdx_offset = kron(transpose(startCRB:startCRB+nPRBs-1),ones(4,1))*12 - 11; % 1-based indexing
                        dmrs_fIdx = repmat(dmrs_fIdx_base,nPRBs,1) + dmrs_fIdx_offset;
                        sym_dmrs_nr = Xtf_nr(dmrs_fIdx, startSym);

                        if SysPar.pucch{idxUe}.nSym > 1
                            if SysPar.pucch{idxUe}.freqHopFlag
                                shPRB = SysPar.pucch{idxUe}.secondHopPRB+1;
                                shCRB = shPRB+SysPar.pucch{idxUe}.BWPStart;
                                data_fIdx_offset = kron(transpose(shCRB:shCRB+nPRBs-1),ones(8,1))*12 - 11; % 1-based indexing
                                data_fIdx = repmat(data_fIdx_base,nPRBs,1) + data_fIdx_offset;
                                dmrs_fIdx_offset = kron(transpose(shCRB:shCRB+nPRBs-1),ones(4,1))*12 - 11; % 1-based indexing
                                dmrs_fIdx = repmat(dmrs_fIdx_base,nPRBs,1) + dmrs_fIdx_offset;
                            end
                            sym_nr = [sym_nr Xtf_nr(data_fIdx, startSym+1)];
                            sym_dmrs_nr = [sym_dmrs_nr Xtf_nr(dmrs_fIdx, startSym+1)];
                        end
                        
                        sym_nr = sym_nr(:);
                        sym_dmrs_nr = sym_dmrs_nr(:);
                    end
                elseif (SysPar.pucch{idxUe}.FormatType == 3)
                    if ~DTX
                        pdu = UE{idxUe}.Phy.Config.pucch.pdu;
                        freqIdx = (12*(pdu.prbStart+pdu.BWPStart)+1):12*(pdu.prbStart+pdu.BWPStart+pdu.prbSize);
                        [F3Para] = deriveF3UciSeqTxSize(0, pdu.BitLenHarq, pdu.BitLenCsiPart1, pdu.BitLenCsiPart2, pdu.freqHopFlag, pdu.AddDmrsFlag, pdu.maxCodeRate, pdu.pi2Bpsk, pdu.NrOfSymbols, pdu.prbSize);
                        SetSymData = F3Para.SetSymData;
                        if pdu.freqHopFlag
                            %% first hop
                            nSymFirstHop = floor(pdu.NrOfSymbols/2); 
                            SetSymDataFirstHop = SetSymData(SetSymData<nSymFirstHop);
                            sym_nr = [sym_nr Xtf_nr(freqIdx, SetSymDataFirstHop+pdu.StartSymbolIndex+1)];
                            %% second hop
                            freqIdx = (12*(pdu.secondHopPRB+pdu.BWPStart)+1):12*(pdu.secondHopPRB+pdu.BWPStart+pdu.prbSize);
                            SetSymDataSecondHop = SetSymData(SetSymData>=nSymFirstHop);
                            sym_nr = [sym_nr Xtf_nr(freqIdx, SetSymDataSecondHop+pdu.StartSymbolIndex+1)];
                        else
                            sym_nr = [sym_nr Xtf_nr(freqIdx, SetSymData+pdu.StartSymbolIndex+1)];
                        end
                        sym_nr = sym_nr(:);
                    end
                else
                    BitLenHarq = SysPar.pucch{idxUe}.BitLenHarq;
                    SRFlag = SysPar.pucch{idxUe}.SRFlag;
                    positiveSR = SysPar.pucch{idxUe}.positiveSR;
                    if ~DTX && (BitLenHarq || (SRFlag && positiveSR))
                        freqIdx = [12*(SysPar.pucch{idxUe}.prbStart+SysPar.pucch{idxUe}.BWPStart)+1:...
                            12*(SysPar.pucch{idxUe}.prbStart+SysPar.pucch{idxUe}.BWPStart+SysPar.pucch{idxUe}.prbSize)];
                        if (SysPar.pucch{idxUe}.FormatType == 0)
                            if SysPar.pucch{idxUe}.freqHopFlag %nSym == 2
                                sym_nr = Xtf_nr(freqIdx, SysPar.pucch{idxUe}.startSym+1);
                                freqIdx = [12*(SysPar.pucch{idxUe}.secondHopPRB+SysPar.pucch{idxUe}.BWPStart)+1:...
                                    12*(SysPar.pucch{idxUe}.secondHopPRB+SysPar.pucch{idxUe}.BWPStart+SysPar.pucch{idxUe}.prbSize)];
                                sym_nr = [sym_nr Xtf_nr(freqIdx, SysPar.pucch{idxUe}.startSym+2)];
                                sym_nr = sym_nr(:);
                            else
                                symIdx = [SysPar.pucch{idxUe}.startSym+1:SysPar.pucch{idxUe}.startSym+SysPar.pucch{idxUe}.nSym];
                                sym_nr = Xtf_nr(freqIdx, symIdx);
                                sym_nr = sym_nr(:);
                            end
                        elseif (SysPar.pucch{idxUe}.FormatType == 1)
                            nSym_data = floor(SysPar.pucch{idxUe}.nSym/2);
                            symIdx = [SysPar.pucch{idxUe}.startSym+2];
                            for symi = 1:(nSym_data-1)
                                symIdx = [symIdx symIdx(end)+2];
                            end
                            if SysPar.pucch{idxUe}.freqHopFlag
                                nSymDataFirstHop = floor(nSym_data/2);
                                sym_nr = Xtf_nr(freqIdx, symIdx(1:nSymDataFirstHop));
                                freqIdx = [12*(SysPar.pucch{idxUe}.secondHopPRB+SysPar.pucch{idxUe}.BWPStart)+1:...
                                    12*(SysPar.pucch{idxUe}.secondHopPRB+SysPar.pucch{idxUe}.BWPStart+SysPar.pucch{idxUe}.prbSize)];
                                sym_nr = [sym_nr Xtf_nr(freqIdx, symIdx(nSymDataFirstHop+1:end))];
                            else
                                sym_nr = Xtf_nr(freqIdx, symIdx);
                            end
                            sym_nr = sym_nr(:);                            
                        end
                    end
                end
                pucch = UE{idxUe}.Phy.Config.pucch;
                carrier = UE{idxUe}.Phy.Config.carrier;
                if ~DTX
                    [sym_5g, sym_dmrs_5g] = hPUCCHGen(pucch, carrier);
                else
                    sym_5g = [];
                    sym_dmrs_5g = [];
                end
                
                % compare data symbols 
                err_sym = sum(abs(sym_nr-sym_5g));
                if err_sym > 1e-4
                    testPass = 0;
                end
                % compare dmrs symbols
                err_sym = sum(abs(sym_dmrs_nr-sym_dmrs_5g));
                if err_sym > 1e-4
                    testPass = 0;
                end
            end
            if ~testPass
                errCnt = errCnt + 1;
            end
        end
        
        if length(CFG{idxSet, 4}) > 1 % mixed formats
            fprintf('%4d', CFG{idxSet, 1});
            fprintf('  mixed   ');
            fprintf('%2d %4d\n', testPass, Detected);
        else
            fprintf('%4d %4d     %2d %4d\n',...
            CFG{idxSet, 1}, CFG{idxSet, 4}, testPass, Detected);
        end
    end
end

fprintf('-----------------------------------------------------------------------------------------\n');
fprintf('Total Compliance TC = %d, det-PASS = %d, det-FAIL = %d, com-PASS = %d, com-FAIL = %d, Total TV generated = %d\n\n', nComp, nComp-detErr, detErr, nComp-errCnt, errCnt, nTV);
toc; 
fprintf('\n');
end
