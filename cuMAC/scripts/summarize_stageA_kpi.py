def format_metric_value(v):
    if v is None:
        return "N/A"
    if isinstance(v, float):
        return f"{v:.10f}"
    return str(v)


def metric_note(path):
    return METRIC_DEFINITIONS.get(path, "")


def format_metric_line(path, value):
    line = f"{path}: {value}"
    note = metric_note(path)
    if note:
        line += f" | {note}"
    return line


def append_definition_rows(lines, scope, fields):
    for field in fields:
        lines.append(f"{scope},{field},{metric_note(f'{scope}.{field}')}")


def first_present_array(parsed, *keys):
    for key in keys:
        if key in parsed:
            return parsed[key]
    return []


def safe_ratio(num, den):
    if den is None or abs(den) <= 1.0e-12:
        return None
    return num / den


def configured_bandwidth_hz(args):
    prbs_per_group = max(int(args.prbs_per_group), 0)
    prg_count = max(int(args.prg_count), 0)
    scs_hz = max(float(args.subcarrier_spacing_hz), 0.0)
    return 12.0 * scs_hz * float(prbs_per_group) * float(prg_count)


def parse_execution_mode(log_text: str):
    m = re.findall(r"cuMAC scheduler pipeline test: execution mode=([A-Za-z0-9_\-]+)", log_text)
    if not m:
        return "both"
    return m[-1].strip().lower()


def build_cpu_gpu_compare(throughput_summary, execution_mode="both"):
    cpu_long = throughput_summary.get("long_term_sum_mbps_cpu", basic_stats([]))
    gpu_long = throughput_summary.get("long_term_sum_mbps_gpu", basic_stats([]))
    cpu_ins = throughput_summary.get("instantaneous_mbps_cpu", basic_stats([]))
    gpu_ins = throughput_summary.get("instantaneous_mbps_gpu", basic_stats([]))
    cpu_ue = throughput_summary.get("per_ue_avg_mbps_cpu", basic_stats([]))
    gpu_ue = throughput_summary.get("per_ue_avg_mbps_gpu", basic_stats([]))

    compare = {
        "instantaneous_mean_mbps_cpu": cpu_ins["mean"],
        "instantaneous_mean_mbps_gpu": gpu_ins["mean"],
        "instantaneous_mean_gpu_minus_cpu_mbps": gpu_ins["mean"] - cpu_ins["mean"],
        "instantaneous_mean_gpu_over_cpu_ratio": safe_ratio(gpu_ins["mean"], cpu_ins["mean"]),
        "long_term_sum_mean_mbps_cpu": cpu_long["mean"],
        "long_term_sum_mean_mbps_gpu": gpu_long["mean"],
        "long_term_sum_mean_gpu_minus_cpu_mbps": gpu_long["mean"] - cpu_long["mean"],
        "long_term_sum_mean_gpu_over_cpu_ratio": safe_ratio(gpu_long["mean"], cpu_long["mean"]),
        "long_term_sum_last_mbps_cpu": cpu_long["last"],
        "long_term_sum_last_mbps_gpu": gpu_long["last"],
        "long_term_sum_last_gpu_minus_cpu_mbps": gpu_long["last"] - cpu_long["last"],
        "long_term_sum_last_gpu_over_cpu_ratio": safe_ratio(gpu_long["last"], cpu_long["last"]),
        "per_ue_avg_mean_mbps_cpu": cpu_ue["mean"],
        "per_ue_avg_mean_mbps_gpu": gpu_ue["mean"],
        "per_ue_avg_mean_gpu_minus_cpu_mbps": gpu_ue["mean"] - cpu_ue["mean"],
        "per_ue_avg_mean_gpu_over_cpu_ratio": safe_ratio(gpu_ue["mean"], cpu_ue["mean"]),
    }
    if execution_mode == "gpu":
        compare["disabled_in_gpu_only"] = True
        compare["disabled_reason"] = (
            "gpu-only mode disables the independent CPU reference scheduler path; "
            "CPU-side throughput arrays are shadow/replay bookkeeping and should not be interpreted as a baseline comparison."
        )
    else:
        compare["disabled_in_gpu_only"] = False
    return compare


