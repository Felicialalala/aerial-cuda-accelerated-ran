def merge_ue_goodput(ue_kpi, ue_goodput):
    for row in ue_kpi:
        gp = ue_goodput.get(row["ue_id"], {})
        row["goodput_bytes"] = gp.get("goodput_bytes")
        row["goodput_mbps"] = gp.get("goodput_mbps")
    return ue_kpi


